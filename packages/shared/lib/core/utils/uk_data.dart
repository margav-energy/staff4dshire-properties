  // Get bank name from sort code
  static String? getBankFromSortCode(String sortCode) {
    // Normalize sort code format (remove spaces, dashes, ensure XX-XX-XX)
    final normalized = _formatSortCode(sortCode);
    if (normalized == null) return null;
    
    // Try exact match first
    if (sortCodeToBank.containsKey(normalized)) {
      return sortCodeToBank[normalized];
    }
    
    // Try prefix match (first 2 digits)
    final prefix = normalized.substring(0, 2);
    for (final entry in sortCodeToBank.entries) {
      if (entry.key.startsWith(prefix)) {
        return entry.value;
      }
    }
    
    return null;
  }

  // Format sort code to XX-XX-XX format
  static String? _formatSortCode(String sortCode) {
    // Remove all non-digits
    final digits = sortCode.replaceAll(RegExp(r'[^\d]'), '');
    
    if (digits.length != 6) return null;
    
    return '${digits.substring(0, 2)}-${digits.substring(2, 4)}-${digits.substring(4, 6)}';
  }

  // Format sort code for display
  static String formatSortCode(String sortCode) {
    final formatted = _formatSortCode(sortCode);
    return formatted ?? sortCode;
  }

  // Format UK postcode - capitalize letters
  static String formatUKPostcode(String postcode) {
    // Remove all spaces first
    final cleaned = postcode.replaceAll(' ', '').toUpperCase();
    
    // UK postcode format: typically last 3 characters are separated
    // Patterns: AN NAA, ANN NAA, AAN NAA, AANN NAA, ANA NAA, AANA NAA
    if (cleaned.length >= 5) {
      final last3 = cleaned.substring(cleaned.length - 3);
      final firstPart = cleaned.substring(0, cleaned.length - 3);
      return '$firstPart $last3';
    }
    
    return cleaned;
  }

  // Validate UK account number (8 digits)
  static bool isValidAccountNumber(String accountNumber) {
    final digits = accountNumber.replaceAll(RegExp(r'[^\d]'), '');
    return digits.length == 8;
  }
